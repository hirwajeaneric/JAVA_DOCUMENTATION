    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        Vaccination vac = new Vaccination();
        VaccinationController cont = new VaccinationController();

        vac.setCitizenId(id.getText());
        vac.setFullName(names.getText());
        vac.setPhone(phone.getText());
        vac.setStatus(status());

        java.sql.Date dates = new java.sql.Date(vaccinationDayDateChooser.getDate().getTime());
        LocalDate date = dates.toLocalDate();//This is the date on which a person was vaccinated.
        
        LocalDate now = LocalDate.now(); //This is the date of today
        Period period = Period.between(date, now);//Here we were trying to find the period past since a person was vaccinated.
        int days = period.getDays(); //We get the date from that period
        
        if (days > 1) {
            vac.setVaccinationDate(date);

            if (brand.getSelectedItem().toString().equals("Pfizer")) {
                vac.setBrand(VaccineBrand.PFIZER);
            }
            if (brand.getSelectedItem().toString().equals("Astrazenica")) {
                vac.setBrand(VaccineBrand.ASTRAZENICA);
            }
            if (brand.getSelectedItem().toString().equals("Moderna")) {
                vac.setBrand(VaccineBrand.MODERNA);
            }
            cont.newVaccination(vac);
            
            clear();
            displayVaccine();
        } else {
            JOptionPane.showMessageDialog(this, "You can't be vaccinated in the future you dumb asshole");
        }
    }//GEN-LAST:event_saveActionPerformed

    private void sortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sortActionPerformed
        table.setRowCount(0);
        List<Vaccination> ls = new VaccinationController().allVaccination();
        String choose = (String) sort.getSelectedItem();
        for (Vaccination vc : ls) {
            if (vc.getStatus().equalsIgnoreCase(choose)) {
                table.insertRow(table.getRowCount(), new Object[]{
                    vc.getCitizenId(),
                    vc.getFullName(),
                    vc.getPhone(),
                    vc.getBrand(),
                    vc.getStatus(),
                    vc.getVaccinationDate()
                });
            }
        }
    }//GEN-LAST:event_sortActionPerformed

