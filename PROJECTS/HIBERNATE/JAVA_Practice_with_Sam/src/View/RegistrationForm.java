    public String status() {
        String status = null;
        if (vaccinated.isSelected()) {
            status = "Vaccinated";
        }
        if (unvaccinated.isSelected()) {
            status = "Unvaccinated";
        }
        return status;
    }

    public void displayVaccine() {
        table.setRowCount(0);
        List<Vaccination> ls = new VaccinationController().allVaccination();
        
        ls.forEach((vc) -> {
            LocalDate dob = vc.getVaccinationDate();
            LocalDate now = LocalDate.now();
            Period period = Period.between(dob, now);
            int age = period.getDays();
            table.insertRow(table.getRowCount(), new Object[]{
                vc.getCitizenId(),
                vc.getFullName(),
                vc.getPhone(),
                vc.getBrand(),
                vc.getStatus(),
                vc.getVaccinationDate(),
                age
            });
        });
    }

    public void clear() {
        id.setText("");
        names.setText("");
        phone.setText("");
        brand.setSelectedItem("");
    }

